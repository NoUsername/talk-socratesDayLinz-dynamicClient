package at.paukl.dynclient.common.model;

/**
 * @author Paul Klingelhuber
 */
public class ViewElement {
    String type;
    String id;

    public ViewElement() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
